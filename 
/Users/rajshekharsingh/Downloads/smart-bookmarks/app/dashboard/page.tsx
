'use client'

import { createClient } from '@/lib/supabase/client'
import { redirect } from 'next/navigation'
import { useEffect, useState } from 'react'
import BookmarksList from '@/components/BookmarksList'
import AddBookmarkForm from '@/components/AddBookmarkForm'
import LogoutButton from '@/components/LogoutButton'

type Bookmark = {
  id: string
  title: string
  url: string
  created_at: string
  user_id: string
}

export default function DashboardPage() {
  const supabase = createClient()
  const [user, setUser] = useState<any>(null)
  const [bookmarks, setBookmarks] = useState<Bookmark[]>([])
  const [loading, setLoading] = useState(true)

  useEffect(() => {
    async function loadData() {
      const { data: { user } } = await supabase.auth.getUser()
      
      if (!user) {
        redirect('/')
        return
      }

      setUser(user)

      // Fetch initial bookmarks
      const { data } = await supabase
        .from('bookmarks')
        .select('*')
        .order('created_at', { ascending: false })

      setBookmarks(data || [])
      setLoading(false)
    }

    loadData()
  }, [supabase])

  const handleBookmarkAdded = (newBookmark: Bookmark) => {
    console.log('handleBookmarkAdded called with:', newBookmark)
    // Instantly add the new bookmark to the list for immediate feedback
    setBookmarks((current) => {
      const updated = [newBookmark, ...current]
      console.log('Updated bookmarks:', updated)
      return updated
    })
  }

  if (loading || !user) {
    return (
      <div className="min-h-screen bg-gray-50 flex items-center justify-center">
        <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-blue-600"></div>
      </div>
    )
  }

  // Get user avatar from email
  const userInitial = user.email?.charAt(0).toUpperCase() || 'U'
  const userName = user.email?.split('@')[0] || 'User'

  return (
    <div className="min-h-screen bg-gray-50">
      {/* Header - Browser-style */}
      <header className="bg-white shadow-sm border-b border-gray-200 sticky top-0 z-50">
        <div className="max-w-5xl mx-auto px-4">
          {/* Top Bar */}
          <div className="flex items-center justify-between h-14">
            {/* Logo and Title */}
            <div className="flex items-center gap-3">
              <div className="flex items-center justify-center w-8 h-8 bg-gradient-to-br from-blue-500 to-indigo-600 rounded-lg">
                <img src="/bookmark_icon.svg" alt="Smart Bookmarks" className="w-5 h-5" />
              </div>
              <span className="font-semibold text-gray-800">Smart Bookmarks</span>
            </div>

            {/* User Menu */}
            <div className="flex items-center gap-3">
              <div className="hidden md:flex items-center gap-2 text-sm text-gray-600">
                <span className="max-w-[150px] truncate">{user.email}</span>
              </div>
              <div className="relative group">
                <button className="flex items-center gap-2 p-1 rounded-full hover:bg-gray-100 transition-colors">
                  <div className="w-8 h-8 bg-gradient-to-br from-blue-400 to-indigo-500 rounded-full flex items-center justify-center text-white font-medium text-sm">
                    {userInitial}
                  </div>
                </button>
                <div className="absolute right-0 top-full mt-1 w-48 bg-white rounded-lg shadow-lg border border-gray-200 py-1 opacity-0 invisible group-hover:opacity-100 group-hover:visible transition-all z-50">
                  <div className="px-4 py-2 border-b border-gray-100">
                    <p className="text-sm font-medium text-gray-900">{userName}</p>
                    <p className="text-xs text-gray-500 truncate">{user.email}</p>
                  </div>
                  <LogoutButton />
                </div>
              </div>
            </div>
          </div>

          {/* Navigation Tabs */}
          <div className="flex items-center gap-1 -mb-px">
            <button className="px-4 py-3 text-sm font-medium text-blue-600 border-b-2 border-blue-600 flex items-center gap-2">
              <img src="/bookmark_icon.svg" alt="" className="w-4 h-4" />
              All Bookmarks
            </button>
          </div>
        </div>
      </header>

      {/* Main Content */}
      <main className="max-w-5xl mx-auto px-4 py-6">
        {/* Add Bookmark Section - Inline Style */}
        <div className="bg-white rounded-xl shadow-sm border border-gray-200 mb-6 overflow-hidden">
          <div className="p-4 border-b border-gray-100 flex items-center gap-2">
            <div className="w-6 h-6 bg-blue-100 rounded flex items-center justify-center">
              <svg className="w-4 h-4 text-blue-600" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                <path strokeLinecap="round" strokeLinejoin="round" d="M12 4v16m8-8H4" />
              </svg>
            </div>
            <h2 className="font-medium text-gray-800">Add new bookmark</h2>
          </div>
          <AddBookmarkForm onBookmarkAdded={handleBookmarkAdded} />
        </div>

        {/* Bookmarks Section */}
        <div className="bg-white rounded-xl shadow-sm border border-gray-200 overflow-hidden">
          <div className="p-4 border-b border-gray-100 flex items-center justify-between">
            <div className="flex items-center gap-2">
              <div className="w-6 h-6 bg-indigo-100 rounded flex items-center justify-center">
                <img src="/bookmark_icon.svg" alt="" className="w-4 h-4" />
              </div>
              <h2 className="font-medium text-gray-800">
                Your Bookmarks
                <span className="ml-2 text-sm text-gray-500 font-normal">
                  ({bookmarks?.length || 0})
                </span>
              </h2>
            </div>
            <div className="flex items-center gap-2 text-xs text-gray-500">
              <span className="flex items-center gap-1">
                <span className="w-2 h-2 bg-green-500 rounded-full animate-pulse"></span>
                Live sync enabled
              </span>
            </div>
          </div>
          <BookmarksList initialBookmarks={bookmarks} userId={user.id} />
        </div>
      </main>

      {/* Footer */}
      <footer className="max-w-5xl mx-auto px-4 py-6 text-center">
        <p className="text-gray-400 text-sm">
          Powered by Supabase • Built with Next.js
        </p>
        <p className="text-gray-300 text-xs mt-1">
          Made by Raj Shekhar Singh • © 2026 Smart Bookmarks. All rights reserved.
        </p>
      </footer>
    </div>
  )
}

